namespace MyData.Properties
{
    /// <summary>
    /// Version number of the assembly.
    /// </summary>
    internal static class AssemblyVersion
    {
        /// <summary>
        /// Main version string with major, minor and patch version numbers.
        /// </summary>
        public const string Main = "0.1.0";

        /// <summary>
        /// Pre-release version suffix.
        /// An empty value indicated a stable version, otherwise is signifies a pre-release version.
        /// Should always start with a hypen (-) if non-empty.
        /// </summary>
        public const string PreRelease = "-alpha";
    }
}
